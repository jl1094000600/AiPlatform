CREATE TABLE IF NOT EXISTS ai_dataset (
  id BIGINT NOT NULL AUTO_INCREMENT,
  tenant_id BIGINT NOT NULL DEFAULT 1,
  dataset_code VARCHAR(50) NOT NULL,
  dataset_name VARCHAR(100) NOT NULL,
  description TEXT,
  category VARCHAR(50),
  format VARCHAR(20) NOT NULL,
  size BIGINT,
  file_path VARCHAR(500),
  record_count INT DEFAULT 0,
  field_schema TEXT,
  status TINYINT DEFAULT 1,
  owner_id BIGINT,
  create_time DATETIME DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  is_deleted TINYINT DEFAULT 0,
  PRIMARY KEY (id),
  UNIQUE KEY uk_dataset_code (tenant_id, dataset_code),
  KEY idx_dataset_tenant (tenant_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS ai_evaluation (
  id BIGINT NOT NULL AUTO_INCREMENT,
  tenant_id BIGINT NOT NULL DEFAULT 1,
  evaluation_code VARCHAR(50) NOT NULL,
  evaluation_name VARCHAR(200),
  description TEXT,
  dataset_id BIGINT NOT NULL,
  agent_id BIGINT NOT NULL,
  batch_code VARCHAR(50),
  criteria_config TEXT,
  result_data MEDIUMTEXT,
  total_score DOUBLE,
  status TINYINT DEFAULT 0,
  total_samples INT DEFAULT 0,
  completed_samples INT DEFAULT 0,
  success_samples INT DEFAULT 0,
  failed_samples INT DEFAULT 0,
  cancel_requested TINYINT DEFAULT 0,
  retry_of_id BIGINT,
  error_message TEXT,
  start_time DATETIME,
  end_time DATETIME,
  executor_id BIGINT,
  create_time DATETIME DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  is_deleted TINYINT DEFAULT 0,
  PRIMARY KEY (id),
  UNIQUE KEY uk_evaluation_code (tenant_id, evaluation_code),
  KEY idx_evaluation_tenant (tenant_id),
  KEY idx_evaluation_dataset (dataset_id),
  KEY idx_evaluation_agent (agent_id),
  KEY idx_evaluation_batch (batch_code)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS ai_evaluation_sample (
  id BIGINT NOT NULL AUTO_INCREMENT,
  tenant_id BIGINT NOT NULL DEFAULT 1,
  evaluation_id BIGINT NOT NULL,
  sample_index INT NOT NULL,
  input_data MEDIUMTEXT,
  expected_output MEDIUMTEXT,
  actual_output MEDIUMTEXT,
  score DOUBLE,
  duration_ms BIGINT,
  status TINYINT DEFAULT 0,
  error_message TEXT,
  metrics_data TEXT,
  create_time DATETIME DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (id),
  UNIQUE KEY uk_evaluation_sample (evaluation_id, sample_index),
  KEY idx_sample_tenant (tenant_id),
  KEY idx_sample_evaluation (evaluation_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;

CREATE TABLE IF NOT EXISTS ai_evaluation_criteria (
  id BIGINT NOT NULL AUTO_INCREMENT,
  tenant_id BIGINT NOT NULL DEFAULT 1,
  criteria_code VARCHAR(50) NOT NULL,
  criteria_name VARCHAR(100) NOT NULL,
  description TEXT,
  type VARCHAR(20),
  formula VARCHAR(500),
  weight DOUBLE DEFAULT 1.0,
  thresholds VARCHAR(200),
  status TINYINT DEFAULT 1,
  create_time DATETIME DEFAULT CURRENT_TIMESTAMP,
  update_time DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  is_deleted TINYINT DEFAULT 0,
  PRIMARY KEY (id),
  UNIQUE KEY uk_criteria_code (tenant_id, criteria_code),
  KEY idx_criteria_tenant (tenant_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4;
