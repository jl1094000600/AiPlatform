const getInitialLocale = () => {
  if (typeof localStorage === 'undefined') return 'zh'
  return localStorage.getItem('locale') || 'zh'
}

export const locale = ref(getInitialLocale())

export const setLocale = (nextLocale) => {
  locale.value = nextLocale === 'en' ? 'en' : 'zh'
  if (typeof localStorage !== 'undefined') {
    localStorage.setItem('locale', locale.value)
  }
}

export const t = (key) => messages[locale.value]?.[key] || messages.zh[key] || key

export const useI18n = () => ({
  locale,
  t,
  setLocale,
  isChinese: computed(() => locale.value === 'zh')
})
